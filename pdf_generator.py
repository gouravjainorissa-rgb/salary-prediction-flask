from reportlab.pdfgen import canvas
from io import BytesIO


def generate_pdf(name, age, education, experience, salary):

    buffer = BytesIO()

    pdf = canvas.Canvas(buffer)

    pdf.setTitle("Salary Prediction Report")

    pdf.setFont("Helvetica-Bold", 18)
    pdf.drawString(150, 800, "Salary Prediction Report")

    pdf.setFont("Helvetica", 12)

    pdf.drawString(50, 740, f"Name : {name}")
    pdf.drawString(50, 710, f"Age : {age}")
    pdf.drawString(50, 680, f"Education : {education}")
    pdf.drawString(50, 650, f"Experience : {experience} Years")

    pdf.line(50, 625, 550, 625)

    pdf.setFont("Helvetica-Bold", 14)
    pdf.drawString(50, 590, "Predicted Monthly Salary")

    pdf.setFont("Helvetica-Bold", 20)
    pdf.drawString(50, 555, f"₹ {salary:,.0f}")

    pdf.setFont("Helvetica", 10)
    pdf.drawString(50, 80, "Generated by AI Salary Prediction System")
    pdf.drawString(50, 60, "Developed by Gourav Jain")

    pdf.save()

    buffer.seek(0)

    return buffer